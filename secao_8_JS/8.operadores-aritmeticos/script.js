var num1 = 9
var num2 = 2

//precedência -> () 
console.log(`A soma entre ${num1} + ${num2} é: ` + (num1 + num2)) 
console.log(`A subtração entre ${num1} + ${num2} é: ` + (num1 - num2)) 
console.log(`A multiplicação entre ${num1} + ${num2} é: ` + (num1 * num2)) 
console.log(`A divisão entre ${num1} + ${num2} é: ` + (num1 / num2)) 
console.log(`O módulo (resto da divisão) entre ${num1} + ${num2} é: ` + (num1 % num2)) 
console.log(`O incremento de ${num1} é: ` + (++num1)) 
console.log(`O decremento de ${num1} é: ` + (--num1))

